//Q.2   write a program to perform arithmetic operator on two number

#include <iostream>
using namespace std;
int main() {
    int x,y,sum,sub,mul,divied,mod;
cout<<"enter the value of x and y :";
cin>>x>>y;
sum=x+y;
sub=x-y;
mul=x*y;
divied=x/y;
mod=x%y;
cout<<"sum :"<<sum<<endl;
cout<<"sub :"<<sub<<endl;
cout<<"mul :"<<mul<<endl;
cout<<"divied :"<<divied<<endl;
cout<<"modulus :"<<mod<<endl;
    return 0;
}
